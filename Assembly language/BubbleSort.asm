.MODEL SMALL
.STACK 100H
.DATA
ARRAY DB '8574764636'



.CODE
MAIN PROC
    
MOV AX,@DATA
MOV DS,AX

MOV SI,0
MOV DI,1

OUTER_LOOP:
 CMP SI,9
 JE INIT
INNER_LOOP:
MOV AL,ARRAY[SI]
CMP AL,ARRAY[DI]
JL INCREAMENTER
XCHG AL,ARRAY[DI]
MOV ARRAY[SI],AL

INCREAMENTER:
CMP DI,9
JE TESTER 
INC DI
JMP INNER_LOOP

TESTER:

INC SI
MOV DI,SI
INC DI
JMP OUTER_LOOP

 INIT:
 MOV SI,0
 JMP PRINT
 
 PRINT:
   MOV DL,ARRAY[SI]
   MOV AH,02
   INT 21H
   CMP SI,9
   JG PASS
   INC SI
   JMP PRINT
   
   PASS:
   


MAIN ENDP
END MAIN