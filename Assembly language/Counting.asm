.MODEL SMALL
.STACK 100H
.DATA 

MSG DW 'ENTER THE INPUT STRING : $'
SMSG DW 10,13,'TOTAL NUMBER OF SPACES : ','$'
DMSG DW 10,13,'TOTAL NUMBER OF DIGITS : ','$'
VMSG DW 10,13,'TOTAL NUMBER OF VOWELS : ','$'

SPACES DB ?
DIGITS DB ?
VOWELS DB ?
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV SPACES,0
MOV DIGITS,0
MOV VOWELS,0
LEA DX,MSG
MOV AH,9
INT 21H
LEVEL:
MOV AH,1
INT 21H
CMP AL,'$'
JE EXIT
CMP AL,' '
JE SPA

CMP AL,'0'
JE DIGIT
CMP AL,'1'
JE DIGIT
CMP AL,'2'
JE DIGIT
CMP AL,'3'
JE DIGIT
CMP AL,'4'
JE DIGIT
CMP AL,'5'
JE DIGIT
CMP AL,'6'
JE DIGIT
CMP AL,'7'
JE DIGIT
CMP AL,'8'
JE DIGIT
CMP AL,'9'
JE DIGIT

CMP AL,'A'
JE VOWEL
CMP AL,'E'
JE VOWEL
CMP AL,'I'
JE VOWEL
CMP AL,'O'
JE VOWEL
CMP AL,'U'
JE VOWEL

CMP AL,'a'
JE VOWEL
CMP AL,'e'
JE VOWEL
CMP AL,'i'
JE VOWEL
CMP AL,'o'
JE VOWEL
CMP AL,'u'
JE VOWEL
JMP LEVEL

SPA:
INC SPACES
JMP LEVEL

DIGIT:
INC DIGITS
JMP LEVEL

VOWEL:
INC VOWELS
JMP LEVEL
 
EXIT:
;displaying how many spaces in the input string
MOV AH,9
LEA DX,SMSG
INT 21H
MOV AH,02
ADD SPACES,48
MOV DL,SPACES
INT 21H
;displaying how many digits are in the input string
MOV AH,9
LEA DX,DMSG
INT 21H
MOV AH,02
ADD DIGITS,48
MOV DL,DIGITS
INT 21H
;displaying how many vowels are in the input string
MOV AH,9
LEA DX,VMSG
INT 21H
MOV AH,02
ADD VOWELS,48
MOV DL,VOWELS
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN